Map::Map()
{
    TileMap = TextureManager::LoadTexture("Assets/bottom_tiles.png");
    LoadMap(LvL);
}

void Map::LoadMap(int Array[20][25])
{
    for(int Row = 0; Row < 20; Row++)
    {
        for(int Column = 0; Column < 25; Column++)
        {
            TheMap[Row][Column] = Array[Row][Column];
        }
    }
}

void Map::DrawMap()
{
    int Type = 0;
    Source.w = Destination.w = 32;
    Source.h = Destination.h = 32;
    for(int Row = 0; Row < 20; Row++)
    {
        for(int Column = 0; Column < 25; Column++)
        {
            Type = TheMap[Row][Column];
            Destination.x = Column*32;
            Destination.y = Row*32;
            switch(Type)
            {
                case 0:
                    Source.x = 64;
                    Source.y = 32;
                    break;
                case 1:
                    Source.x =0;
                    Source.y = 0;
                    break;
                case 2:
                    Source.x = 128;
                    Source.y = 32;
                    break;
            }
            TextureManager::Draw(TileMap, Source, Destination);
        }
    }

    
}
